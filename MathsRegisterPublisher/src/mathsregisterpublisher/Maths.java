package mathsregisterpublisher;

public class Maths {
	
	private String maths_ID;
	private String maths_name;
	private String maths_duration;
	private String maths_grade;
	private String maths_price;
	
	
	public Maths(String maths_ID, String maths_name, String maths_duration, String maths_grade, String maths_price) {
		super();
		this.maths_ID = maths_ID;
		this.maths_name = maths_name;
		this.maths_duration = maths_duration;
		this.maths_grade = maths_grade;
		this.maths_price = maths_price;
	}


	public String getMaths_ID() {
		return maths_ID;
	}


	public void setMaths_ID(String maths_ID) {
		this.maths_ID = maths_ID;
	}


	public String getMaths_name() {
		return maths_name;
	}


	public void setMaths_name(String maths_name) {
		this.maths_name = maths_name;
	}


	public String getMaths_duration() {
		return maths_duration;
	}


	public void setMaths_duration(String maths_duration) {
		this.maths_duration = maths_duration;
	}


	public String getMaths_grade() {
		return maths_grade;
	}


	public void setMaths_grade(String maths_grade) {
		this.maths_grade = maths_grade;
	}


	public String getMaths_price() {
		return maths_price;
	}


	public void setMaths_price(String maths_price) {
		this.maths_price = maths_price;
	}
	
	
	
	
	

}
